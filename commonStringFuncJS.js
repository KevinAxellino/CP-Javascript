// split(): Splits a string into an array of substrings.
const words = str.split(' ');

// join(): Joins an array of strings into a single string.
const sentence = words.join(' ');

// substring(): Extracts a part of a string between two indices.
const part = str.substring(1, 4);

// slice(): Similar to substring(), but also works on arrays.
const slicedStr = str.slice(1, 4);

// indexOf(): Finds the index of the first occurrence of a substring.
const index = str.indexOf('a');

// trim(): Removes whitespace from both ends of a string.
const trimmed = str.trim();

// replace(): Replaces parts of a string with another string or regex.
const newStr = str.replace('old', 'new');